        key = tuple(sorted((c, n)))
        e = edges.setdefault(key, dict(a=key[0], b=key[1], directions={}))
        e["directions"][f"{c}→{n}"] = dict(breath=bn, lift=l, p=p, stable=bool(st), tier=tier, shared=sh)
